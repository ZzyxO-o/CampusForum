-- 讨论帖表创建SQL
-- 表名: discussions
-- 描述: 存储用户发布的讨论帖信息
USE `campus_forum`;

CREATE TABLE IF NOT EXISTS `discussions` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '讨论ID，主键，自增',
  `title` VARCHAR(200) NOT NULL COMMENT '讨论标题，不能为空',
  `content` TEXT NOT NULL COMMENT '讨论内容，不能为空',
  `annex_url` VARCHAR(255) NULL COMMENT '附件URL，逗号分隔',
  `user_id` BIGINT NOT NULL COMMENT '发布者ID，外键关联users表',
  `category` VARCHAR(20) NOT NULL COMMENT '讨论分类：question-疑问, discussion-讨论, share-分享, announcement-公告',
  `tags` VARCHAR(500) NULL COMMENT '标签，逗号分隔',
  `status` VARCHAR(20) NOT NULL DEFAULT 'active' COMMENT '讨论状态：active-活跃, closed-隐藏, deleted-已删除',
  `view_count` INT NOT NULL DEFAULT 0 COMMENT '浏览量',
  `reply_count` INT NOT NULL DEFAULT 0 COMMENT '回复数量',
  `like_count` INT NOT NULL DEFAULT 0 COMMENT '点赞数量',
  `favorite_count` INT NOT NULL DEFAULT 0 COMMENT '收藏数量',
  `created_time` DATETIME NULL COMMENT '创建时间',
  `updated_time` DATETIME NULL COMMENT '更新时间',
  PRIMARY KEY (`id`),
  INDEX `idx_user_id` (`user_id` ASC) COMMENT '用户ID索引',
  INDEX `idx_category` (`category` ASC) COMMENT '分类索引',
  INDEX `idx_status` (`status` ASC) COMMENT '状态索引',
  INDEX `idx_created_time` (`created_time` ASC) COMMENT '创建时间索引',
  CONSTRAINT `fk_discussions_user_id` FOREIGN KEY (`user_id`) REFERENCES `users` (`id`) ON DELETE CASCADE
) COMMENT = '讨论帖表';

